def merge(ids, pair, idx):
    #in the list of ids(tokens or integers), replace all consecutive occurrences of pair with the new int idx(256)
    newids = []
    i = 0
    while i < len(ids):
        if i < len(ids) - 1 and ids[i] == pair[0] and ids[i + 1] == pair[1]:
            newids.append(idx)
            i = i + 2
        else:
            newids.append(ids[i])
            i = i + 1
    return newids

